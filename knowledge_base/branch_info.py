from typing import Optional, Dict, Any, List

# -------- Helpers (tanpa sinonim/normalisasi berat) --------

def _fallback_key(name: str) -> str:
    """Fallback ringan hanya untuk berjaga-jaga jika upstream lupa meng-kanonisasi."""
    return name.lower().strip().replace(" ", "_")

def _pretty(s: str) -> str:
    return s.replace("_", " ").title()

def _format_branch_info(data: Dict[str, Any], key: str) -> str:
    """Format output default yang natural."""
    nama = data.get("nama", _pretty(key))
    alamat = data.get("alamat", "Alamat tidak tersedia.")
    kategori = data.get("kategori", "Kategori tidak tersedia.")
    return f"{nama} ({kategori}) berlokasi di:\n{alamat}"

def _list_available(entity_type: str) -> List[str]:
    """Daftar nama canonical yang tersedia untuk entity_type tertentu."""
    if entity_type not in branch_info:
        return []
    return list(branch_info[entity_type].keys())

# -------- Core API (dipakai nlp_processing) --------

def get_branch_detail(entity_type: str, name: str, info_type: Optional[str] = None) -> str:
    """
    Ambil detail entity (branch/capem/branch_kas/atm).
    Asumsi: `name` sudah canonical dari preprocessing.
    """
    entity_type = (entity_type or "").lower()

    if entity_type not in branch_info:
        return f"Maaf, jenis entitas '{entity_type}' tidak dikenali."

    data_group = branch_info[entity_type]

    # Coba exact key dulu (mengandalkan canonical dari preprocessing)
    key = name
    if key not in data_group:
        # Fallback super-ringkas jika upstream lupa canonical
        fb = _fallback_key(name)
        if fb in data_group:
            key = fb
        else:
            # Beri saran nama yang tersedia (maks 10 biar ringkas)
            available = _list_available(entity_type)[:10]
            hint = ", ".join(_pretty(k) for k in available) if available else "-"
            return (
                f"Maaf, saya tidak menemukan informasi untuk {entity_type} bernama '{name}'.\n"
                f"Contoh yang tersedia: {hint}"
            )

    detail = data_group[key]

    # Jika user minta field spesifik
    if info_type:
        if info_type not in detail:
            available_fields = ", ".join(detail.keys())
            return (
                f"Informasi '{info_type}' tidak tersedia untuk {_pretty(key)}.\n"
                f"Pilihan yang tersedia: {available_fields}"
            )
        label = info_type.replace("_", " ").capitalize()
        return f"{label} dari {detail.get('nama', _pretty(key))}: {detail[info_type]}"

    # Jika tidak, tampilkan ringkasan natural
    return _format_branch_info(detail, key)

# -------- Shortcut functions --------

