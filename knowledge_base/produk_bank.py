from typing import Optional, Dict, Any, List
def _fallback_key(name: str) -> str:
    """Fallback super-ringkas kalau upstream lupa canonical."""
    return (name or "").lower().strip().replace(" ", "_")

def _pretty(s: str) -> str:
    return s.replace("_", " ").title()

def _format_list(items: List[Any]) -> str:
    if not items:
        return "-"
    # Jika list of dict → baris per item, key:value dipisahkan koma
    if all(isinstance(i, dict) for i in items):
        lines = []
        for i in items:
            parts = [f"{_pretty(str(k))}: {v}" for k, v in i.items()]
            lines.append("- " + ", ".join(parts))
        return "\n".join(lines)
    # Jika list of str → bullet
    return "- " + "\n- ".join(str(i) for i in items)

def _available_fields(p: Dict[str, Any]) -> str:
    return ", ".join(p.keys())

def _list_all_products() -> str:
    if not products:
        return "Belum ada produk yang terdaftar."
    names = [ _pretty(k) for k in products.keys() ]
    return "Daftar produk tersedia:\n- " + "\n- ".join(names)

def get_product_info(product_key: Optional[str], info_type: Optional[str]):
    """
    Ambil informasi produk.
    - Jika product_key None/blank → kembalikan daftar semua produk.
    - Jika info_type None → ringkasan deskripsi (jika ada) + field yang tersedia.
    """
    # 1) Jika tidak ada product_key → daftar semua produk
    if not product_key or not str(product_key).strip():
        return _list_all_products()

    # 2) Ambil produk (andalkan canonical dari preprocessing)
    product = products.get(product_key)
    if not product:
        # Fallback tipis kalau upstream lupa canonical
        fb = _fallback_key(product_key)
        product = products.get(fb)
        if not product:
            # kasih saran produk yang tersedia
            suggestions = ", ".join(_pretty(k) for k in list(products.keys())[:10])
            return (
                f"Produk dengan nama '{product_key}' tidak ditemukan.\n"
                f"Contoh yang tersedia: {suggestions}"
            )
        product_key = fb  # pakai key fallback

    # 3) Jika info_type kosong → ringkasan default
    if not info_type:
        desc = product.get("description") or product.get("deskripsi")
        if desc:
            return (
                f"{desc}\n\n"
                f"Bidang informasi tersedia: {_available_fields(product)}"
            )
        return f"Bidang informasi tersedia: {_available_fields(product)}"

    # 4) Ambil field spesifik
    info = product.get(info_type)
    if info is None:
        return (
            f"Informasi mengenai '{info_type}' tidak tersedia untuk produk "
            f"{_pretty(product_key)}. Bidang yang ada: {_available_fields(product)}"
        )

    # 5) Format berdasarkan tipe data
    if isinstance(info, list):
        return _format_list(info)

    if isinstance(info, dict):
        # Tampilkan key:value per baris
        parts = [f"- {_pretty(str(k))}: {v}" for k, v in info.items()]
        return "\n".join(parts)

    # String / angka / lainnya
    if info_type in {"description", "deskripsi"}:
        return info
    if info_type == "benefits":
        return "Berikut manfaat yang bisa kamu dapatkan:\n" + _format_list([*info] if isinstance(info, list) else [info])
    if info_type == "requirements":
        return "Persyaratan yang perlu dipenuhi antara lain:\n" + _format_list([*info] if isinstance(info, list) else [info])

    return str(info)
